			<li>In July, HBO SIGNATURE, HBO FAMILY and HITS are launched for the first time in Thailand as part of the HBO Package through TrueVisions and also in Sri Lanka on Dialog TV and Lanka Broadband Networks.</li>
			<li>HBO Asia celebrates its 15TH anniversary.</li>
		</ul>
	</div>



	<div class="miles">
