			<li>In April, HBO SIGNATURE, HBO FAMILY and HBO HITS are launched in the Philippines as part of THE HBO/MAX PAK through SkyCable.</li>
			<li>In May, HBO SIGNATURE, HBO FAMILY and HBO HITS are launched for the first time in Taiwan.</li>
