		<ul>
			<li>In March, CINEMAX relaunches to be Asia's most exciting movie channel for hits, action and suspense movies, complete with new tagline of 'Get Into The Movies' and a brand-new on-air identity.</li>
			<li>The HBO channel receives the prestigious Superbrands� Award in Singapore.</li>
			<li>HBO is voted as Asia's best cable and movie channel by cable operators across the region.</li>
		</ul>
	</div>


