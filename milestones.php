		<div class="font_bold">1992</div>
		<ul>
			<li>HBO Asia, backed by Paramount and Warner Bros, launches an 18-hour-a-day home movie channel in Asia, securing carriage on SCV in Singapore.</li>
			<li>The English-language service is distributed via Palapa B2P.</li>
		</ul>
	</div>

	<div class="miles">
		<div class="font_bold">1993</div>
		<ul>
			<li>HBO Asia's regional headquarters is officially opened at New Tech Park by Singapore's Minister for Information and the Arts, Brigadier-General George Yeo. HBO Asia, TVB, ESPN, Turner and ABC announce a broadcasters' alliance.</li>
			<li>HBO becomes available in Thailand and the Philippines.</li>
		</ul>
	</div>


	<div class="miles">
		<div class="font_bold">1994</div>
		<ul>
			<li>HBO launches in Taiwan.</li>
			<li>After 20 months on air, HBO shifts to a fully programmed 24-hour service.</li>
			<li>In a landmark deal, HBO Asia concludes exclusive licensing deals with Sony Pictures Entertainment and Universal Studios, who also become equity partners.   The four HBO Asia partner studios jointly claim 19 of the top 25 films released in the United States during 1993 and 1994.</li>
			<li>HBO becomes available in Indonesia.</li>
		</ul>
	</div>


	<div class="miles">
		<div class="font_bold">1995</div>
		<ul>
			<li>HBO starts the year with a subscriber base of 600,000, doubling its earlier record in the previous year and now covers eight markets in Asia.</li>
			<li>HBO adds Bahasa Indonesia subtitles to its full broadcast schedules in addition to closed-captioned subtitles for Thailand and Taiwan that were made available in 1994.</li>
			<li>HBO becomes available in Hong Kong and Malaysia and launches in three-star and above standard hotels and foreign housing compounds in China.</li>
		</ul>
	</div>


	<div class="miles">
		<div class="font_bold">1996</div>
		<ul>
			<li>HBO doubles subscription figures to 1.1 million subscribers.</li>
			<li>Disney subsidiaries, Hollywood Pictures and Touchstone become exclusive HBO Asia suppliers.</li>
			<li>With its lead movie channel in a strong position, HBO Asia launches CINEMAX, a channel for a wide range of entertaining movies.  Agreements are soon signed with operators in Singapore, Taiwan, Thailand and the Philippines.</li>
		</ul>
	</div>


	<div class="miles">
		<div class="font_bold">1997</div>
		<ul>
			<li>Dan Murrell becomes HBO Asia's Managing Director, replacing CEO and President Bill Hooks who returns to HBO in New York after five years at the helm.</li>
			<li>HBO Asia celebrates its fifth anniversary.</li>
			<li>HBO expands to 18 Asian countries.</li>
		</ul>
	</div>


	<div class="miles">
		<div class="font_bold">1998</div>
		<ul>
			<li>HBO Asia is granted Ministry of Radio, Film and Television approval to distribute its CINEMAX channel in China to three-star and above standard hotels and foreign housing compounds.</li>
			<li>CINEMAX launches on ASTRO in Malaysia.</li>
			<li>HBO Asia signs an exclusive deal with DreamWorks SKG.</li>
			<li>HBO and CINEMAX launch in Sri Lanka.</li>
		</ul>
	</div>


	<div class="miles">
		<div class="font_bold">1999</div>
		<ul>
			<li>HBO Asia opens a representative office in Taiwan.</li>
			<li>Dan Murrell is appointed CEO and President of HBO Asia.</li>
			<li>HBO has 3.5 million subscribers by year end.</li>
		</ul>
	</div>


	<div class="miles">
		<div class="font_bold">2000</div>
		<ul>
			<li>CINEMAX launches in Hong Kong as a premium service, alongside HBO.</li>
			<li>An HBO South Asia service is launched in Bangladesh, India, Pakistan and Maldives.  The HBO channel in India and Maldives is ad-supported.</li>
			<li>James Marturano is appointed Executive Vice President of HBO Asia and HBO South Asia's Managing Director.</li>
		</ul>
	</div>


