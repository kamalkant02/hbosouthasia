			<li>HBO retains overall lead for the third year running in the 2009 Pay TV Operators Survey conducted by Fusion Consulting and ContentAsia. HBO tops the movies genre, overall marketing and sales support, positive feedback from subscribers and best value for money categories.</li>
			<li>HBO is the first regional movie channel to broadcast in high definition in Asia in October. HBO HD launches on nowTV in Hong Kong, StarHub in Singapore, Cignal in the Philippines, and on kbro in Taiwan.</li>
			<li>In October, the television premiere of Kung Fu Panda is available for the first time in dual audio languages in Hong Kong, Indonesia, Malaysia and Singapore.</li>
			<li>In November, HBO Asia becomes the exclusive distributing agent for Warner TV and BabyFirst in the Asia-Pacific region.</li>
			<li>HBO HD launches on Astro in Malaysia in December.</li>
		</ul>
