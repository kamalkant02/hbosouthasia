		<ul>
			<li>HBO Asia celebrates ten years of entertaining Asia.</li>
			<li>HBO Asia launches Asia's first fully 'tapeless' system, one that fully digitalizes and archives its extensive library of movies and promotional on-air materials.</li>
		</ul>
	</div>


	<div class="miles">
