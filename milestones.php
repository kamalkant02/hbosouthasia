		<ul>
			<li>In January, HBO takes majority stake in HBO Asia. Previously jointly held by HBO, NBC Universal, Paramount Films and Sony Pictures Entertainment, HBO Asia under the new equity structure is owned by HBO and Paramount.</li>
			<li>HBO continues to lead in Asia as the top-ranked channel with the best programming quality, on-air promotions, as well as sales and marketing support, as voted by pay TV operators across the region according to the 2008 Asia Pacific Pay TV survey by Fusion Consulting.</li>
			<li>In September, HBO ON DEMAND is made available to subscribers of THE HBO PAK in Singapore through StarHub Cable Vision.</li>
		</ul>
	</div>



