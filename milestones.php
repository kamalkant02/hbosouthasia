			<li>HBO premieres the most expensive HBO Original Production ever, Band Of Brothers, across Asia. Based on the best-selling novel by the late-historian Stephen Ambrose, Tom Hanks and Steven Spielberg were executive producers for the ten-part World War II miniseries.</li>
			<li>For the second year in a row, HBO sweeps top honors in a trade survey reinforcing HBO as cable operators' top choice for programming, sales and marketing, on-air look, viewer feedback, value for money and most popular channel.</li>
		</ul>
	</div>


	<div class="miles">
