			<li>From April 1, Chinese subtitling is offered in addition to Bahasa Malaysia for HBO subscribers in Malaysia. HBO is the first movie channel to offer dual subtitling in Malaysia.</li>
			<li>In April, HBO is offered on the newly launched direct-to-home (DTH) pay TV operator in the Philippines, Cignal DTV.Philippines.</li>
